package com.app.mycalc;

public class JioOthersHorizontal {

    private String heading;
    private int myImg;

    public JioOthersHorizontal(String heading, int myImg) {
        this.heading = heading;
        this.myImg = myImg;
    }

    public String getHeading() {
        return heading;
    }

    public void setHeading(String heading) {
        this.heading = heading;
    }

    public int getMyImg() {
        return myImg;
    }

    public void setMyImg(int myImg) {
        this.myImg = myImg;
    }
}
